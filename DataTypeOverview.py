#----------------------------
# All data types is Objects
# type ()
#----------------------------

print(type(10)) 
# Print the type of 10 ==> Integer

print(type(19.10))
# Print the type of 19,10 ==> Float

print(type("Hello Python"))
# Print the type of Hello python ==> String

print(type([1, 2, 3, 4]))
# Print the type of [1 2 3 4] ==> List

print(type((1, 2, 3, 4)))
# Print the type of (1 2 3 4) ==> Tuple

print(type({"One":1, "Two":2, "Three":3}))
# Print the type of ({"One":1, "Two":2, "Three":3}) ==> Dictionary

print(type(2 == 2)) # Type : Bool ==> True 
print(3 == 5) # Bool ==> False

